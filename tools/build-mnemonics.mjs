// Build kana-mnemonics.js from the research workflow's raw records.
//   node tools/build-mnemonics.mjs
// Reads data/gen/kana-mnemonics.raw.json (gitignored), normalizes every entry,
// recomputes each example's romaji from its kana (so the data is internally
// consistent by construction), drops examples that contain kanji or don't
// actually contain the target kana, and tops up to 3 examples from the WORDS
// dataset when needed. Emits the browser/node dual-export module.
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { kanaToRomaji } from './kana-romaji.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');

const raw = JSON.parse(readFileSync(join(root, 'data/gen/kana-mnemonics.raw.json'), 'utf8'));

// Load WORDS for example fallback.
const wsrc = readFileSync(join(root, 'words-data.js'), 'utf8');
const { WORDS } = new Function(wsrc + '\nreturn { WORDS };')();

const cp = (c) => c.codePointAt(0);
const isKanji = (c) => (cp(c) >= 0x4e00 && cp(c) <= 0x9fff) || (cp(c) >= 0x3400 && cp(c) <= 0x4dbf) || c === '々';
const hasKanji = (s) => [...s].some(isKanji);
const isKanaWord = (s) => /^[ぁ-ゟァ-ヿー]+$/.test(s);

// A clean romaji we can show: letters only, non-empty.
function cleanRomaji(w) {
  try {
    const r = kanaToRomaji(w);
    return /^[a-z]+$/.test(r) ? r : null;
  } catch { return null; }
}

function buildExamples(kana, rawExamples) {
  const out = [];
  const seen = new Set();
  for (const ex of (rawExamples || [])) {
    const w = (ex.w || '').trim();
    if (!w || hasKanji(w) || !isKanaWord(w)) continue;
    if (!w.includes(kana) && !w.includes(kana.normalize('NFC'))) continue;
    const r = cleanRomaji(w);
    if (!r || seen.has(w)) continue;
    seen.add(w);
    out.push({ w, r, m: (ex.m || '').trim() });
    if (out.length >= 3) break;
  }
  // Top up from WORDS (kana-only readings that contain the kana).
  if (out.length < 3) {
    for (const word of WORDS) {
      if (out.length >= 3) break;
      const wr = word.r || '';
      if (!isKanaWord(wr) || !wr.includes(kana) || seen.has(wr)) continue;
      const r = cleanRomaji(wr);
      if (!r) continue;
      seen.add(wr);
      out.push({ w: wr, r, m: word.m || '' });
    }
  }
  return out;
}

const built = {};
const problems = [];
for (const rec of raw) {
  const kana = rec.kana;
  if (!kana) { problems.push('record with no kana'); continue; }
  const examples = buildExamples(kana, rec.examples);
  if (examples.length === 0) problems.push(`${kana}: no usable example after fallback`);
  built[kana] = {
    mnemonic: (rec.mnemonic || '').trim(),
    emoji: (rec.emoji || '').trim(),
    confusables: (rec.confusables || [])
      .filter((c) => c && c.k && c.hint)
      .map((c) => ({ k: c.k.trim(), hint: c.hint.trim() })),
    examples,
  };
}

// Emit, stable-sorted by Unicode code point for deterministic diffs.
const keys = Object.keys(built).sort((a, b) => a.codePointAt(0) - b.codePointAt(0));
const body = keys.map((k) => {
  const e = built[k];
  const conf = e.confusables.map((c) => `{ k: ${JSON.stringify(c.k)}, hint: ${JSON.stringify(c.hint)} }`).join(', ');
  const exs = e.examples.map((x) => `{ w: ${JSON.stringify(x.w)}, r: ${JSON.stringify(x.r)}, m: ${JSON.stringify(x.m)} }`).join(', ');
  return `  ${JSON.stringify(k)}: {\n` +
    `    mnemonic: ${JSON.stringify(e.mnemonic)},\n` +
    `    emoji: ${JSON.stringify(e.emoji)},\n` +
    `    confusables: [${conf}],\n` +
    `    examples: [${exs}],\n` +
    `  },`;
}).join('\n');

const out =
`// Mnemonic content for Kana mode miss-feedback. Generated by the research
// workflow + tools/build-mnemonics.mjs — DO NOT hand-edit (re-run the build).
// Keyed by a single kana: { mnemonic, emoji, confusables:[{k,hint}], examples:[{w,r,m}] }
// Dakuten/handakuten/yōon derive their mnemonic from the base kana at runtime.
const KANA_MNEMONICS = {
${body}
};

if (typeof window !== 'undefined') { window.KANA_MNEMONICS = KANA_MNEMONICS; }
if (typeof module !== 'undefined' && module.exports) { module.exports = { KANA_MNEMONICS }; }
`;

writeFileSync(join(root, 'kana-mnemonics.js'), out);
console.log(`Built kana-mnemonics.js: ${keys.length} entries`);
if (problems.length) { console.warn('Warnings:\n' + problems.join('\n')); }
